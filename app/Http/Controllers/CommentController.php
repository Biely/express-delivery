     */
    public function store(Request $request,Comment $comment)
    {
        //
        $this->validate($request, [
            'task_id' => 'required|max:50',
            'content' => 'required|max:255'
        ]);
        $data = $request->all();
        $user = Auth::user();
        $comment->task_id = $data['task_id'];
        $comment->content = e($data['content']);
        $comment->user_uuid = $user->uuid;
        $comment->formuser = $user->name;
        if($comment->save()){
          return redirect()->route('subtask.show',$data['task_id'])->with('status', '评论成功');
        }else{
          return back()->withErrors(['评论失败。']);
        }
    }

